// Función que mapea cada ID de la matriz a un color ANSI
void imprimirPixel(int idColor) {
    switch (idColor) {
    case 0: cout << "\033[48;2;15;19;15m  \033[0m"; break;   // 0 = Fondo oscuro
    case 1: cout << "\033[48;2;255;255;255m  \033[0m"; break; // 1 = Blanco (letras)
    case 2: cout << "\033[48;2;255;153;0m  \033[0m"; break;   // 2 = Naranja (flecha)
    case 3: cout << "\033[48;2;34;139;34m  \033[0m"; break;   // 3 = Verde claro (árboles)
    case 4: cout << "\033[48;2;0;100;0m  \033[0m"; break;     // 4 = Verde oscuro (selva)
    case 5: cout << "\033[48;2;30;144;255m  \033[0m"; break;  // 5 = Azul (río)
    case 6: cout << "\033[48;2;220;20;60m  \033[0m"; break;   // 6 = Rojo (guacamayo)
    case 7: cout << "\033[48;2;255;215;0m  \033[0m"; break;   // 7 = Amarillo (guacamayo)
    case 8: cout << "\033[48;2;139;69;19m  \033[0m"; break;   // 8 = Marrón/Negro (bordes y cabaña)
    default: cout << "  "; break;
    }
}

// Función que dibuja el logo en pantalla
void mostrarLogo() {
    cout << "\nCargando sistema Amazonas...\n\n";
    for (int i = 0; i < sizeNombreX; i++) {
        for (int j = 0; j < sizeNombreY; j++) {
            imprimirPixel(matriz_c[i][j]);
        }
        cout << endl;
    }
    cout << "\n========================================";
    cout << "\nPresione ENTER para ingresar al Marketplace...";
    cin.get();
}

int main() {
    // === CONFIGURACIÓN DE CONSOLA PARA HABILITAR COLORES ANSI ===
    HANDLE hOut = GetStdHandle(STD_OUTPUT_HANDLE);
    DWORD dwMode = 0;
    GetConsoleMode(hOut, &dwMode);
    SetConsoleMode(hOut, dwMode | ENABLE_VIRTUAL_TERMINAL_PROCESSING);
    // ============================================================

    limpiarPantalla(); // Función definida en tu Usuario.h
    mostrarLogo();     // Muestra la matriz dibujada

    Inventario miInventario;
    miInventario.cargarProductosIniciales();

    int op;
    do {
        limpiarPantalla();
        cout << "========================================\n";
        cout << "        AMAZONAS - INICIO\n";
        cout << "========================================\n";
        cout << "Que tipo de usuario eres?\n";
        cout << "1. Cliente\n";
        cout << "2. Vendedor\n";
        cout << "3. Salir\n";
        cout << "Opcion: "; cin >> op;

        switch (op) {
        case 1: {
            limpiarPantalla();
            Cliente clienteActivo;
            clienteActivo.login();
            clienteActivo.menu(miInventario);
            break;
        }
        case 2: {
            limpiarPantalla();
            Vendedor vendedorActivo;
            if (vendedorActivo.login()) {
                vendedorActivo.menu(miInventario);
            }
            break;
        }
        }
    } while (op != 3);

    limpiarPantalla();
    cout << "\nHasta pronto!\n";
    return 0;
}